    private void Add_reviews1_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Add_reviews1_btnActionPerformed
        DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
        String name = table.getValueAt(jTable1.getSelectedRow(), 0).toString();
        connect_database con = connect_database.getInstance();
        try {
            ResultSet rs = con.retrivedata("select id from book where book_name='" + name + "'");
            rs.next();
            int book_id = Integer.parseInt(rs.getString("id"));
            globalValue global = globalValue.getInstance();
            global.setBook_id_review(book_id);
        } catch (SQLException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }

        new Reviews().setVisible(true);
        dispose();
    }//GEN-LAST:event_Add_reviews1_btnActionPerformed

