    private void Add_cart1_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Add_cart1_btnActionPerformed
        connect_database con = connect_database.getInstance();
        DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
        int userID = globalValue.getInstance().getUser_id();
        try {
            String book_name = table.getValueAt(jTable1.getSelectedRow(), 0).toString();
            ResultSet rs = con.retrivedata("select id from book where book_name='" + book_name + "'");
            rs.next();
            int book_id = Integer.parseInt(rs.getString("id"));
            String query = "INSERT INTO cart_books values( '" + book_id + "', '" + userID + "')";
            con.queurydata(query);
            JOptionPane.showMessageDialog(null, "Book Added to Cart");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "You are not logged in");
        }
    }//GEN-LAST:event_Add_cart1_btnActionPerformed

    private void Category_filterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Category_filterActionPerformed
        String cname = Category_filter.getSelectedItem().toString();
        try {
            connect_database DB = connect_database.getInstance();
            ResultSet rs = DB.retrivedata("select id from category where name='" + cname + "'");
            rs.next();
            int cid = Integer.parseInt(rs.getString("id"));
            rs = DB.retrivedata("select book_name, author_name, edition, popularity,price,"
                    + " available_units from book where book_category = '" + cid + "'");
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_Category_filterActionPerformed

    private void Popularity_sortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Popularity_sortActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.getDataVector().sort(new Comparator<Vector>() {
            @Override
            public int compare(Vector o1, Vector o2) {
                // Assuming column 3 contains Integer values
                Integer value1 = (Integer) o1.get(3);
                Integer value2 = (Integer) o2.get(3);

                // Change the comparison logic based on your data type
                return value2.compareTo(value1); // Sort in descending order
            }
        });

// Fire table data changes to reflect the sorted order in the table
        model.fireTableDataChanged();

    }//GEN-LAST:event_Popularity_sortActionPerformed

